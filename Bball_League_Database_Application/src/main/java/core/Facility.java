package core;

public class Facility {
	private Integer idFacility;
	private String facilityName;
	private String addressLine1;
	private String addressLine2;
	private String addressLine3;
	private String state;
	private String postalCode;
	private Integer numCourts;
	
	public Facility() {}

	/**
	 * @return the idFacility
	 */
	public Integer getIdFacility() {
		return idFacility;
	}

	/**
	 * @param idFacility the idFacility to set
	 */
	public void setIdFacility(Integer idFacility) {
		this.idFacility = idFacility;
	}

	/**
	 * @return the facilityName
	 */
	public String getFacilityName() {
		return facilityName;
	}

	/**
	 * @param facilityName the facilityName to set
	 */
	public void setFacilityName(String facilityName) {
		this.facilityName = facilityName;
	}

	/**
	 * @return the addressLine1
	 */
	public String getAddressLine1() {
		return addressLine1;
	}

	/**
	 * @param addressLine1 the addressLine1 to set
	 */
	public void setAddressLine1(String addressLine1) {
		this.addressLine1 = addressLine1;
	}

	/**
	 * @return the addressLine2
	 */
	public String getAddressLine2() {
		return addressLine2;
	}

	/**
	 * @param addressLine2 the addressLine2 to set
	 */
	public void setAddressLine2(String addressLine2) {
		this.addressLine2 = addressLine2;
	}

	/**
	 * @return the addressLine3
	 */
	public String getAddressLine3() {
		return addressLine3;
	}

	/**
	 * @param addressLine3 the addressLine3 to set
	 */
	public void setAddressLine3(String addressLine3) {
		this.addressLine3 = addressLine3;
	}

	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}

	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}

	/**
	 * @return the postalCode
	 */
	public String getPostalCode() {
		return postalCode;
	}

	/**
	 * @param postalCode the postalCode to set
	 */
	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}

	/**
	 * @return the numCourts
	 */
	public Integer getNumCourts() {
		return numCourts;
	}

	/**
	 * @param numCourts the numCourts to set
	 */
	public void setNumCourts(Integer numCourts) {
		this.numCourts = numCourts;
	}
	
}
